namespace DNDGame.Core.Enums;

/// <summary>
/// The six ability scores in D&D 5e.
/// </summary>
public enum AbilityType
{
    /// <summary>
    /// Measures physical power.
    /// </summary>
    Strength,
    
    /// <summary>
    /// Measures agility, reflexes, and balance.
    /// </summary>
    Dexterity,
    
    /// <summary>
    /// Measures health, stamina, and vital force.
    /// </summary>
    Constitution,
    
    /// <summary>
    /// Measures reasoning and memory.
    /// </summary>
    Intelligence,
    
    /// <summary>
    /// Measures awareness, intuition, and insight.
    /// </summary>
    Wisdom,
    
    /// <summary>
    /// Measures force of personality, persuasiveness, and leadership.
    /// </summary>
    Charisma
}
